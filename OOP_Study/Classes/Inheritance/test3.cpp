#include <iostream>

using namespace std;

class A
{
protected:
	int b;

public:
	void create(int x)
	{
		b = x + 2;
	}

	void f() const
	{
		cout << "class A:\n b = " << b << endl;
	}

	void g()
	{
		cout << "class A:\n b = " << b << endl;
		b++;
	}
};

class B : public A
{
protected:
	int b;

public:
	void create(int x)
	{
		b = x;
		A::b = x - 2;
	}

	void f() const
	{
		cout << "class B:\n b = " << b << endl;
		cout << "class A:\n b = " << A::b << endl;
	}

	void g()
	{
		A::b++;
		b++;
		cout << "class B:\n b = " << b << endl;
		cout << "A::b = " << A::b << endl;
	}
};

class C : public B
{
protected:
	int b;

public:
	void create(int x)
	{
		b = x;
		A::b = x - 1;
		B::b = x + 2;
	}

	void f() const
	{
		cout << "class C:\n b = " << b << endl;
		cout << "class B:\n b = " << B::b << endl;
		cout << "A::b = " << A::b << endl;
	}

	void g()
	{
		A::b++;
		B::b++;
		b++;
		cout << "class C:\n b = " << b << endl;
		cout << "class B:\n b = " << B::b << endl;
		cout << "A::b = " << A::b << endl;
	}
};

int main()
{
	A a;
	B b;
	C c;
	
	a.create(0);
	b.create(1);
	c.create(2);
	
	cout << "==============================================================================\n";
	a.f();
	a.g();
	cout << "==============================================================================\n";
	
	cout << "==============================================================================\n";
	b.f();
	b.g();
	cout << "==============================================================================\n";

	cout << "==============================================================================\n";
	c.f();
	c.g();
	cout << "==============================================================================\n";

	cout << "==============================================================================\n";
	b.A::create(3);
	b.A::f();
	b.A::g();
	cout << "==============================================================================\n";

	cout << "==============================================================================\n";
	c.A::f();
	c.A::g();
	cout << "==============================================================================\n";

	cout << "==============================================================================\n";
	c.B::f();
	c.B::g();
	cout << "==============================================================================\n";

	return 0;
}